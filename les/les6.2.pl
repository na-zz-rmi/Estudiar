#!/usr/bin/perl -w




use 5.010;

my @wrds = <STDIN>;

foreach my $word(@wrds)
{
    chomp $word;
    $ed{$word} += 1;
}


foreach $word(sort keys %ed)
{
 say "$word $ed{$word}";
}    
__END__
