слову в строке 1 ), а затем выводит сводку с количеством вхождений
каждого слова. (Подсказка: помните, что при использовании undef
как числа Perl автоматически преобразует его в 0. Если понадобит
ся, вернитесь к более раннему упражнению с накоплением суммы.)
Так, на вход поступают слова fred, barney, fred, dino, wilma, fred (каж
дое слово в отдельной строке); на выходе программа должна сооб
щить, что fred встречается 3 раза. Чтобы задание стало более инте
ресным, отсортируйте сводку по ASCII кодам.
